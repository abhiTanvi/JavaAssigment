package temp;
// Java Program to show the use of
// bitwise operators
import java.io.*;

class bitwise 
{
    public static void main(String[] args)
    {
        // Bitwise operators
        int d = 4;
        int e = 2;
      
        System.out.println("d & e : " + (d & e));
        System.out.println("d | e : " + (d | e));
        
    }
}